package com.example;

public class SmartDevice {

    String marca;
    String modelo;
    String color;
    int peso;
    double precio;

    public SmartDevice() {
    }

    public SmartDevice(String marca, String modelo, String color, int peso, double precio) {
        this.marca = marca;
        this.modelo = modelo;
        this.color = color;
        this.peso = peso;
        this.precio = precio;
    }
}
